#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdarg.h>

#include "aos/kernel.h"
#include "ulog/ulog.h"
#include "kws.h"


int application_start(int argc, char **argv)
{
    aos_set_log_level(AOS_LL_DEBUG);

    kws_init();

    while (1) {
        aos_msleep(1000);
    };

    return 0;
}
