static void OLED_GPIO_Set(unsigned char port,unsigned char leve)
{
	switch(port){
		case CLK_PORT:
			oled_gpio.port = OLED_CLK_PIN;
		break;

		case SDA_PORT:
			oled_gpio.port = OLED_SDA_PIN;
		break;

		case RES_PORT:
			oled_gpio.port = OLED_RES_PIN;
		break;

		case DC_PORT:
			oled_gpio.port = OLED_DC_PIN;
		break;

		default:
		break;
	}
	if(leve == 1){
		hal_gpio_output_high(&oled_gpio);
	}
	else if(leve == 0){
		hal_gpio_output_low(&oled_gpio);
	}
}

void OLED_SCLK( uint8_t x )
{
	if( x==1 ){
		OLED_GPIO_Set( CLK_PORT, 1 );
	}
	else if( x==0 ){
		OLED_GPIO_Set( CLK_PORT, 0);
	}		
}

void OLED_SDIN( uint8_t x )
{
	oled_gpio.port = OLED_SDA_PIN;
	if( x==0x80 ){
		hal_gpio_output_high(&oled_gpio);
	}
	else if( x==0 ){
		hal_gpio_output_low(&oled_gpio);
	}	
}

void OLED_RES( uint8_t x )
{
	if( x==1 ){
		OLED_GPIO_Set( RES_PORT, 1 );
	}
	else if( x==0 ){
		OLED_GPIO_Set( RES_PORT, 0);
	}		
}

void OLED_DC( uint8_t x )
{
	if( x==1 ){
		OLED_GPIO_Set( DC_PORT, 1 );
	}
	else if( x==0 ){
		OLED_GPIO_Set( DC_PORT, 0);
	}		
}

void OLED_WR_Cmd( uint8_t cmd )
{
	uint8_t i,k;

	OLED_DC(0);
	for( i=0;i<8;i++ )
	{
		OLED_SCLK(0);		
		k=cmd&(0x80);
		OLED_SDIN(k);
		OLED_SCLK(1);//high valid
		cmd<<=1;
	}
	OLED_DC(1);	
}

void OLED_WR_Data( uint8_t data )
{
	uint8_t i,k;

	OLED_DC(1);
	for( i=0;i<8;i++ )
	{
		OLED_SCLK(0);
		k=data&(0x80);
		OLED_SDIN(k);
		OLED_SCLK(1);
		data<<=1;
	}
	OLED_DC(1);
}
/* refresh */
void OLED_Refresh_GRAM( void )
{
	uint8_t i,j;
	for( i=0;i<8;i++ )
	{
		OLED_WR_Cmd(0xB0+i);
		OLED_WR_Cmd(0x00);
		OLED_WR_Cmd(0x10);
		for( j=0;j<OLED_PAGE_SIZES;j++ )
		{
			OLED_WR_Data(OLED_GRAM[j][i]);
		}
	}
}

/* clear */
void OLED_Clear( void )
{
	uint8_t i,j;
	for( i=0;i<8;i++ )
		for( j=0;j<OLED_PAGE_SIZES;j++ )
			OLED_GRAM[j][i]=0x0;
	OLED_Refresh_GRAM();
}

/* full */
void OLED_Full( void )
{
	uint8_t i,j;
	for( i=0;i<8;i++ )
		for( j=0;j<OLED_PAGE_SIZES;j++ )
			OLED_GRAM[j][i]=0xff;
	OLED_Refresh_GRAM();
}

//mode：0是反显，1是正常显示
void OLED_DrawPoint( uint8_t x,uint8_t y,uint8_t mode )
{
	uint8_t i,j,temp;
	if((x > (OLED_PAGE_SIZES-1)) || (y > 63))return;
	
	i=7-y/8;
	j=y%8;
	temp=0x01<<(7-j);
	if(mode==0)
		OLED_GRAM[x][i]&=~temp;
	else
		OLED_GRAM[x][i]|=temp;
}

void OLED_Show_Char( uint8_t x,uint8_t y,uint8_t chr,uint8_t size,uint8_t mode )
{
	uint8_t temp,t,t1;
	uint8_t y0=y;

	uint8_t csize=(size/8+((size%8)?1:0))*(size/2);

	LOGI("APP", "++++ char=%c size=%d\n",chr,csize);
	chr=chr-' ';
	for( t=0;t<csize;t++ )
	{	
		switch( size )
		{
			case 12:temp=asc2_1206[chr][t];break;//12x6
			//case 16:temp=asc2_1608[chr][t];break;//16x8
			//case 24:temp=asc2_2412[chr][t];break;//24x12
			default:return;
		}
		LOGI("APP", " new char x= %d,y=%d,t =%d %d\n",x,y,t,temp);
		for( t1=0;t1<8;t1++ )
		{
			if( temp&0x80 )
				OLED_DrawPoint( x,y,mode );
			else
				OLED_DrawPoint( x,y,!mode );
			
			temp<<=1;
			y++;
			LOGI("APP", "x= %d,y=%d\n",x,y);
			if(( y-y0 )==size )
			{
				LOGI("APP", "new line\n");
				y=y0;
				x++;
				break;
			}
		}
	}
}

