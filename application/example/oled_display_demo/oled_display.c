void Utral_ctrl(void)
{
	hal_gpio_output_high(&ultra_gpio);
	_udelay(15);
	hal_gpio_output_low(&ultra_gpio);
}

extern uint8_t dis_flag;
static void handle_haas_cmd(char *pwbuf, int blen, int argc, char **argv)
{
	if(0 == strcmp(argv[1],"0")){
		OLED_Show_String(24,12*0,"AliOS Things4.0",12,1);
		OLED_Show_String(24,(12+4)*1,"This is Haas!",12,1);
		OLED_Show_String(24,(12+4)*2,"2000-01-01 00:00",12,1);
		OLED_Show_String(24,(12+4)*3,"T:30^C H:70%",12,1);
		dis_flag = 1;
	}
	else if(0 == strcmp(argv[1],"1")){
		dis_flag = 2;
		OLED_Clear();
		LOGI("APP", "oled clear\n");
	}
	else if(0 == strcmp(argv[1],"2")){
		dis_flag = 2;
		OLED_Full();
		LOGI("APP", "oled full\n");
	}

	else if(0 == strcmp(argv[1],"3")){
		Utral_ctrl();
		LOGI("APP", "utral ctrl!\n");
	}
}
static struct cli_command haas_cmd = {
    .name     = "haas",
    .help     = "haas [read]",
    .function = handle_haas_cmd
};

