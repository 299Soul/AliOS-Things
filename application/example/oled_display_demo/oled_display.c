	oled_gpio.port = OLED_SDA_PIN;
    oled_gpio.config = OUTPUT_PUSH_PULL;
    hal_gpio_init(&oled_gpio);
	oled_gpio.port = OLED_RES_PIN;
    oled_gpio.config = OUTPUT_PUSH_PULL;
